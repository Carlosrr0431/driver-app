/**
 * Busca un barrio por nombre (fuzzy match)
 * @param {string} query - nombre del barrio a buscar
 * @returns {{ name: string, lat: number, lng: number } | null}
 */
export function findBarrio(query) {
  if (!query) return null;
  
  const normalized = query.toLowerCase().trim()
    .replace(/^barrio\s+/i, '')
    .replace(/^villa\s+/i, '')
    .replace(/^loteo\s+/i, '')
    .replace(/^asentamiento\s+/i, '');
  
  // 1. Check aliases first
  if (BARRIO_ALIASES[normalized] !== undefined) {
    const officialName = BARRIO_ALIASES[normalized];
    if (officialName && BARRIO_COORDINATES[officialName]) {
      return {
        name: officialName,
        ...BARRIO_COORDINATES[officialName],
      };
    }
    return null; // null alias means outside city
  }
  
  // 2. Exact match (case-insensitive)
  for (const [name, coords] of Object.entries(BARRIO_COORDINATES)) {
    if (name.toLowerCase() === normalized) {
      return { name, ...coords };
    }
  }
  
  // 3. Partial match - barrio name starts with query
  for (const [name, coords] of Object.entries(BARRIO_COORDINATES)) {
    if (name.toLowerCase().startsWith(normalized)) {
      return { name, ...coords };
    }
  }
  
  // 4. Contains match
  for (const [name, coords] of Object.entries(BARRIO_COORDINATES)) {
    if (name.toLowerCase().includes(normalized)) {
      return { name, ...coords };
    }
  }
  
  return null;
}

/**
 * Busca todos los barrios que coincidan parcialmente
 * @param {string} query
 * @returns {Array<{ name: string, lat: number, lng: number }>}
 */
export function searchBarrios(query) {
  if (!query) return [];
  
  const normalized = query.toLowerCase().trim()
    .replace(/^barrio\s+/i, '')
    .replace(/^villa\s+/i, '')
    .replace(/^loteo\s+/i, '')
    .replace(/^asentamiento\s+/i, '');
  
  // Split query into individual words for fuzzy matching
  const queryWords = normalized.split(/\s+/).filter(w => w.length > 0);
  if (queryWords.length === 0) return [];
  
  const results = [];
  
  for (const [name, coords] of Object.entries(BARRIO_COORDINATES)) {
    const nameLower = name.toLowerCase();
    // All query words must appear in the barrio name (in any order)
    const allMatch = queryWords.every(word => nameLower.includes(word));
    if (allMatch) {
      results.push({ name, ...coords });
    }
  }
  
  // Sort: exact substring match first, then alphabetical
  results.sort((a, b) => {
    const aExact = a.name.toLowerCase().includes(normalized) ? 0 : 1;
    const bExact = b.name.toLowerCase().includes(normalized) ? 0 : 1;
    if (aExact !== bExact) return aExact - bExact;
    return a.name.localeCompare(b.name);
  });
  
  return results;
}
